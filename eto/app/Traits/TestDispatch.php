    public static function _mapDrivers()
    {
        $minutes = 10;
        $users = [];
        $bounds = request('bounds');

        // ETO DEMO start
        $query = \App\Models\User::role('driver.*');
        // ETO DEMO end

        // ->where('last_seen_at', '>=', Carbon::now()->subSeconds($minutes * 60));

        // if ( !empty($bounds) ) {
            // Check the bounds markers only
        // }

        // ETO DEMO start
        // http://www.mapcoordinates.net/en
        // https://www.google.com/maps/d/u/0/edit?hl=en&mid=1vzr8ZF18IS_UVYTbzhS8fnIUJjE
        $coordinates = self::$_coordinates;
        // ETO DEMO end

        // Disable drivers layer (tickbox)

        $status = ['available', 'busy', 'away'];

        foreach($query->get() as $user) {

            // ETO DEMO start
            if ( isset($coordinates[$user->id]) && (
                    is_null($user->last_seen_at) || ( !is_null($user->last_seen_at) && $user->last_seen_at < \Carbon\Carbon::now()->subSeconds(60) )
                ) ) {

                $uc = $coordinates[$user->id];
                $up = session('demo_coordinates_'. $user->id, [0, 'up']);
                $index = (int)$up[0];
                $direction = (string)$up[1];

                if ( isset($uc[$index]) ) {
                    $user->lat = $uc[$index][1];
                    $user->lng = $uc[$index][0];
                    $user->accuracy = 0;
                }

                if ( $index + 1 >= count($uc) ) {
                    $direction = 'down';
                }
                elseif ( $index - 1 <= -1 ) {
                    $direction = 'up';
                }

                if ( $direction == 'up' ) {
                    $index += 1;
                }
                else {
                    $index -= 1;
                }

                session(['demo_coordinates_'. $user->id => [$index, $direction]]);
            }
            // ETO DEMO end

            if ( !is_null($user->lat) && !is_null($user->lng) ) {
                $users[] = (object)[
                    'id' => $user->id,
                    'name' => $user->getName(true),
                    'displayName' => !empty($user->profile->unique_id) ? $user->profile->unique_id : $user->getName(true),
                    'unique_id' => !empty($user->profile->unique_id) ? $user->profile->unique_id : '',
                    'lat' => $user->lat,
                    'lng' => $user->lng,
                    'accuracy' => $user->accuracy,
                    'heading' => $user->heading,
                    'url' => route('admin.users.show', $user->id),
                    'availability' => $status[ rand(0,2) ]
                ];
            }
        }

        return $users;
    }

